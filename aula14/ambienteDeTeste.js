//estrutura WHILE

console.log('estrutura WHILE')
var c = 1
while(c <= 5) {
    console.log(c)
    c++
}

console.log('**********')

//estrutura DO WHILE

console.log('estrutura DO WHILE')
c = 1
do {
    console.log(c)
    c++
}while (c <= 5)


console.log('**********')


// estrutura FOR

console.log('estrutura FOR')
for(c=1; c<=5; c++) {
    console.log(c)
}